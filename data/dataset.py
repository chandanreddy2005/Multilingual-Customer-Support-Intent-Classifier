FOLLOW_UPS = [
    "",
    "please help me quickly",
    "I need support today",
    "can you check this issue",
    "this is urgent",
]


def multilingual_data() -> pd.DataFrame:
    rows = []
    for language, intents in EXAMPLES.items():
        for intent, examples in intents.items():
            for text in examples:
                for suffix in FOLLOW_UPS:
                    full_text = f"{text} {suffix}".strip()
                    rows.append(
                        {
                            "text": full_text,
                            "intent": intent,
                            "language": language,
                        }
                    )
    return pd.DataFrame(rows)


def generate_dataset(output_path: str | os.PathLike[str] = "data/customer_support_data.csv") -> pd.DataFrame:
    df = multilingual_data()
    df = df.sample(frac=1, random_state=42).reset_index(drop=True)
    output = Path(output_path)
    output.parent.mkdir(parents=True, exist_ok=True)
    df.to_csv(output, index=False)
    print(f"Dataset created: {len(df)} samples")
    print(f"Languages: {df['language'].nunique()} | Intents: {df['intent'].nunique()}")
    print(df["intent"].value_counts().sort_index().to_string())
    return df


if __name__ == "__main__":
    generate_dataset()
